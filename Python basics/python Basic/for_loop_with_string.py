x = "CyberSecurity"

# Loop through each index in the string 'x'
for index in range(len(x)):
    # Print the index on the same line, followed by a space
    print(index, end=' ')

# Separate the outputs for clarity
print()  # Just an empty line

# Loop through each character in the string 'x'
for char in x:
    # Print each character on the same line, followed by a space
    print(char, end=' ')
