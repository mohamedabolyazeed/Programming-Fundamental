# Initialize the counter variable n
n = 0

# Start the loop which continues as long as n is less than 5
while n < 5:
    # Print the current value of n
    print("Current number:", n)
    
    # Increment n by 1 to progress towards the loop's end condition
    n += 1  # Each iteration advances n by one step

# Once n reaches 5, the loop exits and the program ends
print("Loop finished. n is now", n)
