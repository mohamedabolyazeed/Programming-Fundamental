# Create a list of numbers from 1 to 5
numbers = [1, 2, 3, 4, 5]

# Start the loop to iterate through each number in the list
for number in numbers:
    # Calculate the square of the current number
    squared = number ** 2
    
    # Print the original number and its square in a formatted message
    print(f"The square of {number} is {squared}")

# Print a final message indicating the loop has finished processing
print("All numbers have been squared!")
