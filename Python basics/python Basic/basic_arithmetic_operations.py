# Addition: Adding 3 and 6
print(3 + 6)  # This will print 9

# Subtraction: Subtracting 6 from 3
print(3 - 6)  # This will print -3

# Division: Dividing 10 by 2
print(10 / 2)  # This will print 5.0

# Integer Division: Dividing 16 by 3 and getting the integer result
print(16 // 3)  # This will print 5

# Exponentiation: Raising 2 to the power of 4
print(2 ** 4)  # This will print 16

# Multiplication: Multiplying 3 by 7
print(3 * 7)  # This will print 21
