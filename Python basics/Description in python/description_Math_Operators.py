# 1. Arithmetic Operators
# + (Addition): Adds two numbers.
sum = 5 + 3  # Example: 5 + 3 = 8

# - (Subtraction): Subtracts the second number from the first number.
difference = 10 - 4  # Example: 10 - 4 = 6

# * (Multiplication): Multiplies two numbers.
product = 7 * 6  # Example: 7 * 6 = 42

# / (Division): Divides the first number by the second number (returns a float).
quotient = 15 / 4  # Example: 15 / 4 = 3.75

# // (Floor Division): Divides the first number by the second number and returns the integer part of the result.
floor_division = 15 // 4  # Example: 15 // 4 = 3

# % (Modulus): Returns the remainder of the division.
remainder = 15 % 4  # Example: 15 % 4 = 3

# ** (Exponentiation): Raises the first number to the power of the second number.
power = 2 ** 3  # Example: 2 ** 3 = 8

# 2. Unary Operators
# - (Unary Minus): Negates a number.
negation = -5  # Example: -5

# + (Unary Plus): Indicates a positive number (typically redundant).
positive = +10  # Example: +10

# 3. Compound Assignment Operators
# += (Addition Assignment): Adds and assigns the result to the variable.
a = 5
a += 3  # Equivalent to: a = a + 3

# -= (Subtraction Assignment): Subtracts and assigns the result to the variable.
b = 10
b -= 4  # Equivalent to: b = b - 4

# *= (Multiplication Assignment): Multiplies and assigns the result to the variable.
c = 7
c *= 2  # Equivalent to: c = c * 2

# /= (Division Assignment): Divides and assigns the result to the variable (returns a float).
d = 20
d /= 4  # Equivalent to: d = d / 4

# //= (Floor Division Assignment): Performs floor division and assigns the result to the variable.
e = 9
e //= 2  # Equivalent to: e = e // 2

# %= (Modulus Assignment): Calculates modulus and assigns the result to the variable.
f = 10
f %= 3  # Equivalent to: f = f % 3

# **= (Exponentiation Assignment): Raises to the power and assigns the result to the variable.
g = 2
g **= 3  # Equivalent to: g = g ** 3
