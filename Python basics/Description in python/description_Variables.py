# Variables in Python

# 1. Variable Declaration
# Variables are used to store data values. In Python, you don't need to declare the type of the variable explicitly; it is determined dynamically.

# Example of variable declaration and initialization:
name = "Mohamed"  # 'name' is a variable that stores a string value
age = 25  # 'age' is a variable that stores an integer value
height = 1.75  # 'height' is a variable that stores a float value

# 2. Variable Naming Rules
# - Variables names must start with a letter (a-z, A-Z) or an underscore (_).
# - The rest of the variable name can include letters, numbers (0-9), and underscores.
# - Variable names are case-sensitive (e.g., 'variable' and 'Variable' are different).

# Example of valid variable names:
first_name = "Alice"
lastName = "Smith"
_age = 30
value1 = 100

# Example of invalid variable names:
# 1name = "Invalid"  # Starts with a number
# first-name = "Invalid"  # Contains a hyphen
# class = "Invalid"  # Reserved keyword

# 3. Variable Assignment
# Variables are assigned values using the equals sign (=). The value on the right is assigned to the variable on the left.

# Example of variable assignment:
x = 10  # 'x' is assigned the value 10
y = x  # 'y' now has the same value as 'x', which is 10

# 4. Multiple Assignment
# You can assign values to multiple variables in one line.

# Example of multiple assignment:
a, b, c = 1, 2, 3  # 'a' is 1, 'b' is 2, 'c' is 3

# 5. Variable Reassignment
# Variables can be reassigned new values at any time.

# Example of reassignment:
x = 10  # Initial assignment
x = 20  # Reassignment
