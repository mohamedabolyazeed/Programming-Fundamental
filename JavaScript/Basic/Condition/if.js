let role = "admin";

// Using if-else instead of switch
if (role === "admin") {
  console.log(`The role is: admin`);
} else if (role === "peter") {
  console.log(`The role is: peter`);
} else if (role === "carlos") {
  console.log(`The role is: carlos`);
} else {
  console.log(`Role not recognized`);
}
