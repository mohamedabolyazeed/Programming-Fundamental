// Absolute Value
console.log(Math.abs(-6)); // Output: 6 → Converts negative number to positive

// Square Root
console.log(Math.sqrt(4)); // Output: 2 → √4 = 2

// Exponentiation
console.log(Math.pow(6, 3)); // Output: 216 → 6³ = 6 * 6 * 6

// Rounding to the Nearest Integer
console.log(Math.round(6.6)); // Output: 7 → Rounds up since 6.6 is closer to 7
console.log(Math.round(6.4)); // Output: 6 → Rounds down since 6.4 is closer to 6

// Flooring and Ceiling
console.log(Math.floor(6.6)); // Output: 6 → Rounds down to the nearest integer
console.log(Math.ceil(6.4)); // Output: 7 → Rounds up to the nearest integer

// Finding Minimum and Maximum
console.log(Math.min(6, 3, 1, 8, 9, 4)); // Output: 1 → Smallest number in the list
console.log(Math.max(6, 3, 1, 8, 9, 4)); // Output: 9 → Largest number in the list

// Generating a Random Number (between 0 and 1)
console.log(Math.random()); // Output: A random decimal between 0 (inclusive) and 1 (exclusive)

// Truncating Numbers (Removing Decimal Part)
console.log(Math.trunc(6.6)); // Output: 6 → Removes decimal part, no rounding
console.log(Math.trunc(6.4)); // Output: 6 → Same as above, just removes decimals
